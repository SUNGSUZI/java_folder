package com.samsung;

public class CompOper {
	
	public void gt(int a, int b) {
		System.out.println(a > b);
	}
	
	public boolean ge(int a, int b) {
		return a >= b;
	}
	
	public void lt(int a, int b) {
		System.out.println(a < b);
	}
	
	public void le(int a, int b) {
		System.out.println( a <= b);
	}

	public void e(int a, int b) {
		System.out.println(a == b);
	}
	
	public void ne(int a, int b) {
		System.out.println(a != b);
	}
	
}
